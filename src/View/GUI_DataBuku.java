    public void kosong() {
        this.tfIdBuku.setText("");
        this.tfJudul.setText("");
        this.tfPengarang.setText("");
        this.tfTahun.setText("");
    }

    public void setTabelModel() throws SQLException {
        this.tblBuku.setModel(this.controller_buku.viewTabel());
    }

    private boolean cekSemuaTextKosong() {
        boolean textKosong = false;
        if (this.tfIdBuku.getText().isEmpty()
                || this.tfJudul.getText().isEmpty()
                || this.tfPengarang.getText().isEmpty()
                || this.tfTahun.getText().isEmpty()) {
            textKosong = true;
        }
        return textKosong;
    }


