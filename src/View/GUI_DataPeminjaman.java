    public void showTablePeminjam() throws SQLException {
        DefaultTableModel dtmPinjam = new DefaultTableModel(new String[]{"ID Peminjaman", "Admin", "Member", "Tanggal Pinjam"}, 0);
        dtmPinjam.setRowCount(0);

        for (Peminjaman dp : this.controller_peminjaman.getDataPeminjaman()) {
            dtmPinjam.addRow(new String[]{dp.getId_Peminjaman().toString(), dp.getAdmin().getNama(), 
                dp.getMember().getNama(), dp.getTanggal_Pinjam().toString()});
        }

        this.tblPeminjaman.setModel(dtmPinjam);
    }

