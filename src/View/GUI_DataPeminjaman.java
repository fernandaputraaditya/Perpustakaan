    private void btnLihatDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLihatDetailActionPerformed
        // TODO add your handling code here:
        try {
            Peminjaman peminjaman = this.controller_peminjaman.getDataPeminjaman().get(this.tblPeminjaman.getSelectedRow());
            String teks = "" + "====================== MEMBER ======================" + "\n"
                    + "ID Member : " + peminjaman.getMember().getId_Member() + "\n"
                    + "Nama : " + peminjaman.getMember().getNama() + "\n"
                    + "Alamat : " + peminjaman.getMember().getAlamat() + "\n"
                    + "Jenis Kelamin : " + peminjaman.getMember().getJenis_Kelamin() + "\n"
                    + "Tanggal Lahir : " + new SimpleDateFormat("dd/MM/yyyy").format(peminjaman.getMember().getTanggal_Lahir()) + "\n"
                    + "No. Telp : " + peminjaman.getMember().getNo_Telp() + "\n\n"
                    + "======================= ADMIN ======================" + "\n"
                    + "ID Admin : " + peminjaman.getAdmin().getId_Admin() + "\n"
                    + "Nama : " + peminjaman.getAdmin().getNama() + "\n"
                    + "Alamat : " + peminjaman.getAdmin().getAlamat() + "\n"
                    + "Jenis Kelamin : " + peminjaman.getAdmin().getJenis_Kelamin() + "\n\n"
                    + "==================== PEMINJAMAN ====================" + "\n"
                    + "ID Peminjaman : " + peminjaman.getId_Peminjaman() + "\n"
                    + "Tanggal Peminjaman : " + new SimpleDateFormat("dd/MM/yyyy").format(peminjaman.getTanggal_Pinjam()) + "\n"
                    + "Peminjaman : " + "\n";
            ArrayList<Detail_Peminjaman> dp = peminjaman.getArrDetail_Peminjaman();
            for (int i = 0; i < dp.size(); i++) {
                teks += "\n" + (i + 1) + ". "
                        + "ID Buku          : " + dp.get(i).getPeminjaman().getBuku().getId_Buku() + "\n"
                        + "    Judul Buku    : " + dp.get(i).getPeminjaman().getBuku().getJudul_Buku() + "\n"
                        + "    Pengarang    : " + dp.get(i).getPeminjaman().getBuku().getPengarang() + "\n"
                        + "    Tahun Terbit : " + dp.get(i).getPeminjaman().getBuku().getTahun_Terbit();
            }

            this.taLihatDetail.setText(teks);
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnLihatDetailActionPerformed

