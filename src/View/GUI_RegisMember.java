    public void kosong() {
        this.tfId.setText("");
        this.tfNama.setText("");
        this.taAlamat.setText("");
        this.cbJenisKelamin.setSelectedItem("");
        this.tfTglLahir.setText("");
        this.tfNoTelp.setText("");
        this.tfNik.setText("");
        this.tfEmail.setText("");
        this.tfPass.setText("");
    }

    private boolean cekSemuaTextKosong() {
        boolean textKosong = false;
        if (this.tfId.getText().isEmpty()
                || this.tfNama.getText().isEmpty()
                || this.taAlamat.getText().isEmpty()
                || this.tfTglLahir.getText().isEmpty()
                || this.tfNoTelp.getText().isEmpty()
                || this.tfNik.getText().isEmpty()
                || this.tfEmail.getText().isEmpty()
                || this.tfPass.getText().isEmpty()) {
            textKosong = true;
        }
        return textKosong;
    }

