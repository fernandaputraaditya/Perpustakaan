    private void btnRegistrasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrasiActionPerformed
        // TODO add your handling code here:
        if (cekSemuaTextKosong()) {
            JOptionPane.showMessageDialog(null, "Isi Semua Kolom Isian Terlebih Dahulu",
                    "Gagal Registrasi", WARNING_MESSAGE);
        } else {
            int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Data Yang Anda Input Sudah Benar?",
                    "Konfirmasi", OK_CANCEL_OPTION, QUESTION_MESSAGE);
            if (konfirmasi == 0) {
                int id_member = Integer.parseInt(this.tfId.getText());
                String nama = this.tfNama.getText();
                String alamat = this.taAlamat.getText();
                String jenis_kelamin = this.cbJenisKelamin.getSelectedItem().toString();
                String tanggal_lahir = this.tfTglLahir.getText();
                String no_telp = this.tfNoTelp.getText();
                int nik = Integer.parseInt(this.tfNik.getText());
                String email = this.tfEmail.getText();
                String password = this.tfPass.getText();
                this.controller_member.insert(id_member, nama, alamat, jenis_kelamin, tanggal_lahir, no_telp, nik, email, password);
                }
            this.kosong();
            JOptionPane.showMessageDialog(null, "Registrasi Berhasil");
        }
    }//GEN-LAST:event_btnRegistrasiActionPerformed

